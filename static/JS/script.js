function opti(){
    var op = document.querySelector("#option")
  
if(op.style.display =="none"){
      op.style.display ="block"}
  
  
    else(
      op.style.display ="none"
    )
}
  
function sumir(){
     var sumir = document.querySelector("#option")
  
  
    if(sumir.style.display =="block"){
      sumir.style.display ="none"
    }
}







